#ifndef BIBLIOTECA_H_INCLUDED
#define BIBLIOTECA_H_INCLUDED

/** \brief sumar dos numero
 *
 * \param crear una variable resultado
 * \param  guardar la suma en la variable resultado
 * \return retornar resultado
 *
 */
int suma_numero(int numero1, int numero2);
/** \brief restar dos numero
 *
 * \param   crear una variable resultado
 * \param  guardar la suma en la variable resultado
 * \return retornar resultado
 *
 */
int resta_numero(int numero1, int numero2);
/** \brief multiplicar dos numero
 *
 * \param   crear una variable resultado
 * \param  guardar la suma en la variable resultado
 * \return retornar resultado
 *
 */

int mutiplicacion_numero(int numero1, int numero2);
/** \brief dividir dos numeros
 *
 * \param   no divide si el segundo numero es 0
 * \return retornar el resultado de la divicion
 *
 */

int divicion_numero(int numero1, int numero2);
/** \brief Calcula el factorial
 * \param  si num es 0 o 1 devuelve 1
 *  \param calcula el factorial
 * \return  el resultado del factorial
 *
 */
int factorial_numero(int numero1, int numero2);


#endif // BIBLIOTECA_H_INCLUDED
